function setLocale(locale) {
    gameState.locale = UI_TRANSLATIONS[locale] ? locale : 'pt-BR';
    localizeGameData();
    applyStaticTranslations();
    if (typeof renderJourney === 'function') renderJourney();
    if (typeof renderTechniques === 'function') renderTechniques();
    if (typeof renderDaoTree === 'function') renderDaoTree();
    if (typeof updateUI === 'function') updateUI();
    if (typeof updateAudioHud === 'function') updateAudioHud();
}

function initializeLocalization() {
    captureBaseGameText();
    localizeGameData();
    applyStaticTranslations();

    const languageSelect = document.getElementById('language-select');
    if (languageSelect && !languageSelect.dataset.bound) {
        languageSelect.dataset.bound = '1';
        languageSelect.addEventListener('change', event => {
            setLocale(event.target.value);
            saveGame();
        });
    }
}

