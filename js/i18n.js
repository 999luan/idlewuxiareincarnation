function captureBaseGameText() {
    if (BASE_GAME_TEXT) return;
    BASE_GAME_TEXT = {
        journeyStartLog: UI_TRANSLATIONS['pt-BR'].journey_start_log,
        subRealms: JSON.parse(JSON.stringify(GAME_DATA.subRealms)),
        realms: JSON.parse(JSON.stringify(GAME_DATA.realms)),
        buildings: JSON.parse(JSON.stringify(GAME_DATA.buildings)),
        techniques: JSON.parse(JSON.stringify(GAME_DATA.techniques)),
        metaUpgrades: JSON.parse(JSON.stringify(GAME_DATA.metaUpgrades)),
        journeyActions: JSON.parse(JSON.stringify(GAME_DATA.journeyActions))
    };
}

function localizeGameData() {
    captureBaseGameText();
    const locale = getCurrentLocale();
    const translations = CONTENT_TRANSLATIONS[locale] || {};

    ['subRealms', 'realms', 'buildings', 'techniques', 'metaUpgrades', 'journeyActions'].forEach(group => {
        Object.keys(BASE_GAME_TEXT[group]).forEach(id => {
            const baseEntry = BASE_GAME_TEXT[group][id];
            Object.assign(GAME_DATA[group][id], baseEntry);
            const translatedEntry = translations[group]?.[id];
            if (translatedEntry) {
                Object.assign(GAME_DATA[group][id], translatedEntry);
            }
            if (group === 'journeyActions' && JOURNEY_LOG_TRANSLATIONS[locale]?.[id]) {
                GAME_DATA[group][id].log = JOURNEY_LOG_TRANSLATIONS[locale][id];
            }
        });
    });
}

